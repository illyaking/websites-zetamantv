                <h1>ZETAMANTV TEAMS WITH SUPERHEROES ANONYMOUS TO FILM REAL-LIFE SUPERHERO CONVENTION</h1>
                    <p>PORTLAND, OR – ZetamanTV, the locally produced reality web series that chronicles the crime fighting, comic book drawing and fundraising adventures of Portland’s real-life superhero Illya “Zetaman” King and his superhero-posse, is set to shoot new episodes at the 4th annual Superheroes Anonymous (SA4) convention in Portland.  The event, which runs September 17-19, provides a rare opportunity for real-life superheroes (RLSH) from around the world to meet, attend workshops, and discuss the state of the community.</p> 
                    <p>Please see www.zetaman.tv</p>
                    <p>Zetaman was selected by his RLSH peers to host this year’s Superheroes Anonymous, a responsibility that he takes as seriously as his costumed activism.  “I was blown away and humbled when they selected me,” admits Zetaman.  “Most RLSH only know each other through online chats and pictures.  This could be the only chance    they ever get to meet each other in-person.”</p>
                    <p>ZetamanTV producer Allan Luebke plans to roll cameras on the entire three-day event.  “This is a once-in-a-lifetime opportunity,” Luebke says excitedly.  “ZetamanTV’s core fan base is the real-life superhero community.  Because of ZetamanTV partnering with SA4, those same heroes now have the opportunity to appearance in the show.”</p>
                    <p>Luebke plans to take advantage of the weeks leading up to SA4 by premiering a new episode and new outtake every Wednesday starting August 11 and culminating September 17, the first day of SA4.  The strategy has been a big hit.  The first new episode, which shows Zetaman and superhero teammate Antiman painting their new body armor, received record-setting views in the first day.</p>
